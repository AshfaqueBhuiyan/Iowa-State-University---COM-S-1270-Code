def areaOfRectangle(base, height):
    return base * height

def rectanglePerimeter(length, width):
    return 2 * (length + width)

def areaOfCircle(radius):
    import math
    return math.pi * (radius ** 2)

def circleCircumference(radius):
    import math
    return 2 * math.pi * radius
